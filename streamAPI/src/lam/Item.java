package lam;

public class Item {
    private int preco;
    private int quant;


    public Item(int preco, int quant){
        this.preco = preco;
        this.quant = quant;
    }


	public int getPreco() {
		return preco;
	}


	public void setPreco(int preco) {
		this.preco = preco;
	}


	public int getQuant() {
		return quant;
	}


	public void setQuant(int quant) {
		this.quant = quant;
	}


    



}
